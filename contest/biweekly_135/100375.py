class Solution:
    def losingPlayer(self, x: int, y: int) -> str:
        # x个75, y个10, 115 = 75 + 4 * 10
        names = ["Bob", "Alice"]
        turns = min(x, y // 4)
        return names[turns % 2]


if __name__ == '__main__':
    print(
        Solution().losingPlayer(4, 11)
    )
